public class Etudiant {
	// Attribut d'un étudiant
	private String nom, prenom;

	/** constructeur vide d'un étudiant
	 * 
	 */
	public Etudiant() {
	
	}
	
	/** accesseur en lecture de l'attribut nom
	 * 
	 * @return nom
	 */
	public String nom() {
		return nom;
	}
	
	/** accesseur en lecture de l'attribut prenom
	 * 
	 * @return prenom
	 */
	public String prenom() {
		return prenom;
	}
	

}
